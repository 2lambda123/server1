function Item:switchDoor(movecreatures)
	local doordata = otstd.doors[self:getItemID()] 
	
	if self:isDoor() then
		if movecreatures ~= nil and movecreatures then
			local tile = self:getParentTile()
			if doordata.closed == false then
				creatures = tile:getCreatures()
				for _, creature in pairs(creatures) do
					if(doordata.horizontal) then
						if not creature:walk(SOUTH) and not creature:walk(NORTH) then
							local npos = tile:getPosition()
							-- Couldn't walk there, try teleport south
							npos.y = npos.y + 1
							if creature:moveTo(tile:getPosition()) then
								-- Move succeded
							else
								-- Teleport north instead?
								npos.y = npos.y - 2
								if creature:moveTo(creature:getMasterPos()) then
									break
								end
							end
						end
					else
						-- vertical
						if not creature:walk(EAST) and not creature:walk(WEST) then
							local npos = tile:getPosition()
							-- Couldn't walk there, try teleport east
							npos.x = npos.x + 1
							if creature:moveTo(tile:getPosition()) then
								-- Move succeded
							else
								-- Teleport south instead?
								npos.x = npos.x - 2
								if creature:moveTo(creature:getMasterPos()) then
									break
								end
							end
						end
					end
				end
			end
		end
		self:setItemID(doordata.newid)
		return true
	end
	return false
end

function Item:isDoorClosed()
	local doordata = otstd.doors[self:getItemID()]
	if doordata ~= nil then
		return doordata.closed
	end
	return false
end

function Item:isDoorOpen()
	local doordata = otstd.doors[self:getItemID()]
	if doordata ~= nil then
		return not doordata.closed
	end
	return false
end

function Item:openDoor(movecreatures)
	if self:isDoorClosed() then
		self:switchDoor(movecreatures)
	end
end

function Item:closeDoor(movecreatures)
	if self:isDoorOpen() then
		self:swtichDoor(movecreatures)
	end
end

function Item:isDoor()
	return otstd.doors[self:getItemID()] ~= nil
end

-- Generic door switch function
function otstd.doorCallback(evt)
	local door = evt.item
	if door:isDoor() then
		door:switchDoor(true)
	end
end

-- Register listeners!
do
	for doorid, doortype in pairs(otstd.doors) do
		doortype.listener = 
			registerGenericOnUseItemListener("itemid", doorid, otstd.doorCallback)
	end
end


